const mongoose = require("mongoose");

const toolSchema = mongoose.Schema({
  id: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    default: "",
  },
});

const Tool = mongoose.model("Tool", toolSchema);

module.exports = Tool;
